        ),
      ],
      ),
    );
  }

  Widget _buildSnippetList() {
    if (_isLoading && _snippets.isEmpty) {
      return const Center(
        child: LoadingIndicator(
          message: 'Ê≠£Âú®Âä†ËΩΩÁâáÊÆµ...',
          size: 32,
        ),
      );
    }

    if (_snippets.isEmpty) {
      return EmptyStatePlaceholder(
        icon: Icons.bookmark_border,
        title: 'ÊöÇÊó†ÁâáÊÆµ',
        message: _searchText.isNotEmpty ? 'Êú™ÊâæÂà∞ÂåπÈÖçÁöÑÁâáÊÆµ' : 'ËøòÊ≤°ÊúâÂàõÂª∫‰ªª‰ΩïÁâáÊÆµ\nÁÇπÂáª‰∏äÊñπ"ÂàõÂª∫ÁâáÊÆµ"ÊåâÈíÆÂàõÂª∫Á¨¨‰∏Ä‰∏™ÁâáÊÆµ',
      );
    }

    return ListView.builder(
      controller: _scrollController,
      padding: const EdgeInsets.all(16),
      itemCount: _snippets.length + (_hasMore ? 1 : 0),
      itemBuilder: (context, index) {
        if (index == _snippets.length) {
          return const Padding(
            padding: EdgeInsets.all(16),
            child: Center(
              child: LoadingIndicator(size: 24),
            ),
          );
        }

        final snippet = _snippets[index];
        return _buildSnippetItem(snippet);
      },
    );
  }

