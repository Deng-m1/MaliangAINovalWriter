          searchHint: 'ÊêúÁ¥¢ÁâáÊÆµ...',
          newButtonText: 'ÂàõÂª∫ÁâáÊÆµ',
          onSearchChanged: _onSearchChanged,
          onFilterPressed: _showFilterDialog,
          onNewPressed: _showCreateSnippetDialog,
          onSettingsPressed: _showSnippetSettings,
